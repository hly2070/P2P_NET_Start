/**********************************************************************
* 函数名称：RemovePeerByName
* 功能描述：从peer链表中删除一个节点
* 输入参数：userName	Peer节点携带的用户ID
* 输出参数：无
* 返 回 值：无
* 其它说明： 
* 修改日期		  版本号	 修改人 	  修改内容
* -----------------------------------------------
* 2018/05/04	  V1.0		 hly2070	
***********************************************************************/
void RemovePeerByName(UserList *ptPeerList, S8 *strUserName);

