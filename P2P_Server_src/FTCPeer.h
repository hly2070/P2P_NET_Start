/**********************************************************************
* 函数名称：CheckPeerListByName
* 功能描述：查看用户是否存在节点链表中
* 输入参数：userName	Peer节点携带的用户ID
* 输出参数：无
* 返 回 值：节点是否存在的BOOL类型变量
* 其它说明： 
* 修改日期		  版本号	 修改人 	  修改内容
* -----------------------------------------------
* 2018/05/04	  V1.0		 hly2070	
***********************************************************************/
