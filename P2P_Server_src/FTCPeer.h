using namespace std;

//客户节点信息
struct stUserListNode
{
	S8 userName[MAX_NAME_SIZE];			// 节点的名字
	U32 uiIP;				//  节点的IP
	U16 usPORT;			// 节点的 PORT

	stUserListNode()
	{
		bzero(this, sizeof(*this));
	}
};

typedef list<stUserListNode*> UserList;

typedef struct
{
	BOOL bLogin;
	S8 sMyName[MAX_NAME_SIZE];	
} st_Peer;

/**************************************************************************
 *                         全局函数                                    *
 **************************************************************************/
/**********************************************************************
* 函数名称：GetPeerByName
* 功能描述：获取peer节点信息
* 输入参数：userName Peer节点携带的用户ID
* 输出参数：无
* 返 回 值：节点
* 其它说明： 
* 修改日期		  版本号	 修改人 	  修改内容
* -----------------------------------------------
* 2018/05/04	  V1.0		 hly2070	
***********************************************************************/
